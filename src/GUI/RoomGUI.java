    public void showRoom() {
        ArrayList<Room> list = Room.SelectAll();
        model = (DefaultTableModel) tb_room.getModel();
        int i=1;
        model.setColumnIdentifiers(new Object[]{
            "STT", "Tên phòng", "Số giường", "Giá", "Vip", "Tình trạng"
        });
        model.setRowCount(0);
        for (Room s : list) {
            model.addRow(new Object[]{
                i++, s.getName(), s.getBed(), s.getPrice()+".000đ", s.isVip()?"Phòng vip":"Phòng thường", s.isBooked()?"Đã được đặt":"Còn trống"
            });
        }
    }
